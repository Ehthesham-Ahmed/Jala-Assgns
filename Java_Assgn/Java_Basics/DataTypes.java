// 4.  Define variables for different Data Types int, Boolean, char, float, double and print on the Console 

package Java_Assgn.Java_Basics;

public class DataTypes {
    public static void main(String[] args) {
        // syntax for integer: int <var> = <value>;
        int a = 21;
        // syntax for float: float <var> = <value>;
        float b = 15.55f;
        // syntax for double: double <var> = <value>;
        double c = 1245.2568;
        // syntax for char: char <var> = '<value>';
        char d = 'H';
        // syntax for String: String <var> = "<value>";
        String s = "welcome";
        // syntax for boolean: boolean <var> = true or false;
        boolean i = true;

        System.out.println("This is an integer: " + a);
        System.out.println("This is a float: " + b);
        System.out.println("This is a double: " + c);
        System.out.println("This is a char: " + d);
        System.out.println("This is a String: " + s);
        System.out.println("This is a boolean: " + i);
    }
}
